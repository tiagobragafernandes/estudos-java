package onde.dio.operadores.logicos;

public class OperadoresLogicos {

    public static void main(String[] args) {

        boolean a = true;
        boolean b = false;

        //and == &&
        //or == ||
        //or exclusive == ^
        //not == !

        System.out.println(a && b);
        System.out.println(a ^ b);
        System.out.println(a == !b);

    }

}
